    public void rememberme(){
        preferences = Preferences.userNodeForPackage(this.getClass());
        rememberPreference = preferences.getBoolean("RememberMe", Boolean.valueOf(""));
        if (rememberPreference){
        txtusername.setText(preferences.get("username", ""));
        txtpassword.setText(preferences.get("password", ""));
        remember.setSelected(rememberPreference);
        txtpassword.setEchoChar('*');
        }
    }
    
    public void startComponenColor(){
        if(!txtusername.getText().equals("username") && !txtpassword.getText().equals("password")){
        txtusername.setForeground(Color.black);
        txtpassword.setForeground(Color.black);
        
        }
        
    }

